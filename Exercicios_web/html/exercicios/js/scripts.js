var btn = document.querySelector('#show-or-hide');
var btn2 = document.querySelector('#hidis');
var btn3 = document.querySelector('.tree');

btn.addEventListener('click', function() {
    
    if(show-or-hide.style.display === 'block') {
        show-or-hide.style.display = 'none';
    } else {
        show-or-hide.style.display = 'block';
    }

});

btn2.addEventListener('click', function(){
    
    if(hidis.style.display === 'block') {
        hidis.style.display = 'none';
    } else {
        hidis.style.display = 'block';
    }

});

btn3.addEventListener('click', function(){
    
    if(tree.style.display === 'block') {
        tree.style.display = 'none';
    } else {
        tree.style.display = 'block';
    }

});